package com.spring.binding;

public class Income {
	
	private Integer caseNum;
	
	private Integer userId;
	
	private Double salaryIncome;
	
	private Double rentIcome;
	
	private Double propertyIncome;

	public Integer getCaseNum() {
		return caseNum;
	}

	public void setCaseNum(Integer caseNum) {
		this.caseNum = caseNum;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public Double getSalaryIncome() {
		return salaryIncome;
	}

	public void setSalaryIncome(Double salaryIncome) {
		this.salaryIncome = salaryIncome;
	}

	public Double getRentIcome() {
		return rentIcome;
	}

	public void setRentIcome(Double rentIcome) {
		this.rentIcome = rentIcome;
	}

	public Double getPropertyIncome() {
		return propertyIncome;
	}

	public void setPropertyIncome(Double propertyIncome) {
		this.propertyIncome = propertyIncome;
	}
	
	
	
	

}
